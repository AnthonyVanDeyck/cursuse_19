int		ft_sqrt(int nb)
{
	unsigned int p;

	p = 1;
	if (nb < 0)
	{
		return (0);
	}
	while (p * p < (unsigned int)nb)
	{
		p++;
	}
	return ((p * p == (unsigned int)nb) ? p : 0);
}
