int		ft_atoi(char *str)
{
	int p;
	int n;

	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	n = 0;
	while (*str == '-' || *str == '+')
		if (*str++ == '-')
			n = 1 - n;
	p = 0;
	while (*str >= '0' && *str <= '9')
		p = p * 10 + *str++ - '0';
	if (n)
		p *= -1;
	return (p);
}
