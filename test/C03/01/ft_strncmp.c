int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int p;

	p = 0;
	while ((s1[p] != '\0') && (s1[p] == s2[p]) && p < n)
	{
		p++;
	}
	if (p == n)
	{
		return (0);
	}
	return (s1[p] - s2[p]);
}
